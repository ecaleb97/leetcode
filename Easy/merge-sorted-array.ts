  let i = m - 1;
  let j = n - 1;
  let k = m + n - 1;
  while (k >= 0) {
    if (j >= 0) {
      nums1[k] = nums2[j];
      j--;
    } else if (i >= 0) {
      nums1[k] = nums1[i];
      i--;
    }
    k--;
  }
